"""
Soul.md API — read/write Hermes identity files (USER.md + MEMORY.md).
Mirrors the "soul.md" template from Jack Roberts' Hermes OS.
"""
import os
from pathlib import Path
from fastapi import APIRouter, HTTPException
from fastapi.responses import JSONResponse

router = APIRouter(prefix="/api/soul", tags=["soul"])

MEMORIES_DIR = os.path.expanduser("~/.hermes/memories")
USER_PATH = os.path.join(MEMORIES_DIR, "USER.md")
MEMORY_PATH = os.path.join(MEMORIES_DIR, "MEMORY.md")

